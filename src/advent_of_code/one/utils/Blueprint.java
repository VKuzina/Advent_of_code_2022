package advent_of_code.one.utils;

public class Blueprint {
	int or;
	int cr;
	int br_o;
	int br_c;
	int gr_o;
	int gr_b;

	public Blueprint(int or, int cr, int br_o, int br_c, int gr_o, int gr_b) {
		super();
		this.or = or;
		this.cr = cr;
		this.br_o = br_o;
		this.br_c = br_c;
		this.gr_o = gr_o;
		this.gr_b = gr_b;
	}
}
